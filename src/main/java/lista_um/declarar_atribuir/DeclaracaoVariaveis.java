package lista_um.declarar_atribuir;

public class DeclaracaoVariaveis {

    public static int variavelInt = 50;
    public static double variavelDouble = 8.5;
    public static float variavelFloat = 10.5F;
    public static boolean variavelBoolean = false;
    public static long variavelLong = 100000000L;

    public static void main(String[] args) {
        System.out.println("Valor int " + variavelInt);
        System.out.println("Valor double " + variavelDouble);
        System.out.println("Valor float " + variavelFloat);
        System.out.println("Valor boolean " + variavelBoolean);
        System.out.println("Valor long " + variavelLong);
    }
}